// ===== Language Detection & Switching =====
(function() {
  var SUPPORTED = ['pt', 'es', 'en'];
  var STORAGE_KEY = 'yogaoem_lang';

  function detectLang() {
    // 1. URL param
    var urlLang = new URLSearchParams(window.location.search).get('lang');
    if (urlLang && SUPPORTED.indexOf(urlLang) !== -1) return urlLang;

    // 2. User preference
    var saved = localStorage.getItem(STORAGE_KEY);
    if (saved && SUPPORTED.indexOf(saved) !== -1) return saved;

    // 3. Browser language
    var nav = (navigator.language || navigator.userLanguage || 'en').toLowerCase().substring(0, 2);
    if (nav === 'pt') return 'pt';
    if (nav === 'es') return 'es';

    // 4. Default
    return 'en';
  }

  function setLang(lang) {
    if (!I18N[lang]) lang = 'en';
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations(lang);
  }

  function applyTranslations(lang) {
    var dict = I18N[lang] || I18N['en'];

    // Update all [data-i18n] elements
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      var val = dict[key];
      if (val !== undefined) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = val;
        } else {
          el.innerHTML = val;
        }
      }
    });

    // Update html lang attribute
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang === 'es' ? 'es' : 'en';

    // Update lang switcher active state
    document.querySelectorAll('.lang-btn').forEach(function(btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
  }

  // Expose for inline onclick
  window.setLang = setLang;

  // Auto-detect and apply on load
  var lang = detectLang();
  // Defer to after DOM ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function() { setLang(lang); });
  } else {
    setLang(lang);
  }
})();